package poli;

public class operacoes {
	


void somar(double a, double b) {
	System.out.printf("Soma : %f + %f = ", a + b);
}

void somar(int a, double b) {
	System.out.printf("Soma : %f + %f = ", a + b);
}
void somar(int a, int b, int c) {
	System.out.printf("Soma : %f + %f + %f = ", a + b + c);
}
}
